    private void createUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createUserMouseClicked
        // TODO add your handling code here:
        mess.setText("");
        if(!validInput()){
            mess.setText("Bạn cần nhập đầy đủ thông tin");
        }
        else if(!password.getText().equals(validPassword.getText())){
            mess.setText("Mật khẩu và xác nhận mật khẩu cần giống nhau");
        }
        else if(password.getText().length()<6){
            mess.setText("Mật khẩu cần lớn hơn 6 ký tự !");
        }
        else{
            LocalDate dateOfBirthU = LocalDate.of(dateOfBirth.getDate().getYear()+1900,dateOfBirth.getDate().getMonth()+1,dateOfBirth.getDate().getDate());
            User user = new User();
            user.setFullname(fullName.getText());
            user.setGender(gender.getSelectedItem().toString());
            user.setAddress(address.getText());
            user.setDateOfBirth(dateOfBirthU);
            user.setNumberPhone(numberPhone.getText());
            user.setEmail(email.getText());
            user.setUsername(username.getText());
            user.setPassword(password.getText());
            user.setRole("borrower");
            
            UserDAO userDao = new UserDAO();
            
            if(userDao.save(user)){
                JOptionPane.showMessageDialog(null, "Tạo tài khoản cho " + user.getFullname()+" thành công !", "Thành công",JOptionPane.INFORMATION_MESSAGE);
                homeLibrarian.resetTableUsers(homeLibrarian.filterUser);
            }
            else{
                JOptionPane.showMessageDialog(null, "Tạo tài khoản cho " + user.getFullname()+" thất bại !", "Thất bại", JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_createUserMouseClicked

