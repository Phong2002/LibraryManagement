    boolean validInput(){
        if(fullName.getText().equals("")||address.getText().equals("")||numberPhone.getText().equals("")
                ||username.getText().equals("")||password.getText().equals("")||validPassword.getText().equals("")){
            return false;
        }
        return true;
    }
